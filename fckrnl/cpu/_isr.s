%include "fckrnl/asm/helper.asm"

extern isr_handler

global _isr_handler
global _isr_names

_isr_handler:
	cld
	pusha64

	mov rdi, rsp
	call isr_handler
	mov rsp, rax

	popa64
	add rsp, 16

	iretq

%macro ISR_NAME 1
dq _ISR%1
%endmacro

%macro ISR_ERR 1
_ISR%1:
	push %1
	jmp _isr_handler
%endmacro

